%% markov sequence

function s = markovc(n)

d = 0.5;
for i = 1 : n
    if (i == 1)
        s(i) = 1 * normrnd(0, d);
    else
        s(i) = 1 * normrnd(s(i - 1), d);
    end
end
s=s';

